package com.github.MrMks.Werewolf_V3.Resources.Gaming;

public class BaseArgument {
    private int SELF = 0;
    private int TARGET = 0;
    private int KILLER = 0;

    public int getSELF() {
        return SELF;
    }

    public void setSELF(int SELF) {
        this.SELF = SELF;
    }

    public int getTARGET() {
        return TARGET;
    }

    public void setTARGET(int TARGET) {
        this.TARGET = TARGET;
    }

    public int getKILLER() {
        return KILLER;
    }

    public void setKILLER(int KILLER) {
        this.KILLER = KILLER;
    }
}
